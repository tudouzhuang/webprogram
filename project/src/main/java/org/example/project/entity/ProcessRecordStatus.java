package org.example.project.entity;


public enum ProcessRecordStatus {
    /**
     * 草稿 (Draft)
     * 业务含义：设计人员已经创建了《设计过程记录表》，但尚未正式提交给审核流程。
     *          在这个状态下，记录仅对创建者（设计员）可见，可以任意编辑和删除。
     * 触发条件：新记录被创建时的默认状态。
     * 下一步：  设计员点击“提交”后，状态变为 PENDING_REVIEW。
     */
    DRAFT,

    /**
     * 待审核 (Pending Review)
     * 业务含义：设计员已确认设计内容并正式提交，等待审核人员进行审查。
     *          此时，任务的“球”在审核员脚下（由 assignee_id 字段指定）。设计员变为只读权限。
     * 触发条件：1. 设计员从 DRAFT 状态提交。
     *          2. 设计员从 CHANGES_REQUESTED 状态重新提交。
     * 下一步：  审核员处理后，状态可能变为 APPROVED, REJECTED, 或 CHANGES_REQUESTED。
     *          审核员也可以将任务转交（Reassign），此时状态不变，但 assignee_id 改变。
     */
    PENDING_REVIEW,

    /**
     * 要求修改 (Changes Requested) / 已打回
     * 业务含义：审核员审查了设计，但发现需要修改的问题，已将任务“打回”给原始设计员。
     *          此时，任务的“球”回到了设计员脚下（assignee_id 被重置为 creator_id）。
     *          设计员需要根据审核意见进行修改。
     * 触发条件：审核员在审核过程中执行了“打回修改”操作。
     * 下一步：  设计员修改完成后，重新提交，状态变回 PENDING_REVIEW。
     */
    CHANGES_REQUESTED,

    /**
     * 已批准 (Approved) / 审核通过
     * 业务含义：审核员审查了设计，并确认所有内容均符合要求。
     *          这条记录的常规生命周期已经成功结束。通常变为归档状态。
     * 触发条件：审核员在审核过程中执行了“批准”操作。
     * 下一步：  流程结束。但在你的新需求中，此状态下的记录仍可以被有权限的审核员“打回修改”（触发一个新流程）。
     */
    APPROVED,

    /**
     * 已驳回 (Rejected) / 审核不通过
     * 业务含义：审核员审查了设计，并判定该设计存在重大问题或方向性错误，不予通过。
     *          这条记录的生命周期也已结束，但标记为失败。通常意味着需要重新创建一份新的设计记录。
     * 触发条件：审核员在审核过程中执行了“驳回”操作。
     * 下一步：  流程结束。通常此状态为最终状态，不可再编辑。
     */
    REJECTED
}